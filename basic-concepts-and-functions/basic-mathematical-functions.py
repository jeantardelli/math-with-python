"""
This mudule illustrates some of the built-in mathematical functions
readily available in the math library
"""
import math

math.sqrt(4) # 2.0

theta = math.pi / 4
math.cos(theta) # 0.7071067811865476
math.sin(theta) # 0.7071067811865476
math.tan(theta) # 0.9999999999999999

math.asin(-1) # -1.5707963267948966
math.acos(-1) #  3.141592653589793
math.atan(1) # 0.7853981633974483

math.log(10) # 2.302585092994046
math.log(10, 10) # 1.0

math.gamma(5) # 24.0 = 4!
math.erf(2) # 0.9953222650189527

math.comb(5, 2) # 10
math.factorial(5) # 120

math.gcd(2, 4) # 2
math.gcd(2, 3) # 1

nums = [0.1]*10 # list containing 0.1 ten times
sum(nums) # 0.9999999999999999
math.fsum(nums) # 1.0
